package com.demo.model;

public class MapVideoDeviceAndWifiDevice {
	private int id;
	private String wifi_device_id;
	private String video_device_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getWifi_device_id() {
		return wifi_device_id;
	}

	public void setWifi_device_id(String wifi_device_id) {
		this.wifi_device_id = wifi_device_id;
	}

	public String getVideo_device_id() {
		return video_device_id;
	}

	public void setVideo_device_id(String video_device_id) {
		this.video_device_id = video_device_id;
	}
}
